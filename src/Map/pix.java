package Map;

public class pix {

	 private double x;
	private double y;
	
	public pix(double x,double y) {
		
		this.x=x;
		this.y=y;
	}
	public double x() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public double y() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}


}
